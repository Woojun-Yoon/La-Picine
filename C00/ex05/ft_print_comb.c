#include <unistd.h>

void	ft_print_comb(void)
{
	char		n[5];

	n[0] = '0';
	n[3] = ',';
	n[4] = ' ';
	while (n[0] < '7')
	{
		n[1] = n[0] + 1;
		while (n[1] < '9')
		{
			n[2] = n[1] + 1;
			while (n[2] <= '9')
			{
				write(1, n, 5);
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
	write(1, "789", 3);
}
