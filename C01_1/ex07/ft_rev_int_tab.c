void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	cache;

	i = 0;
	j = size - 1;
	while (size > 1)
	{
		cache = tab[i];
		tab[i] = tab[j];
		tab[j] = cache;
		i++;
		j--;
		size -= 2;
	}
}
